//* equal
console.log(3 == 3);
console.log('3' == 3);

//* strict equal (overrides JS coercion)
console.log(3 === 3);
console.log(3 === '3');

//* not equal
console.log('3' != 3);

//* strict not equal
console.log('3' !== 3);

//* greater than
3 > 2;

//* less than
2 < 3;

//* greater than or equal to
3 >= 2;

//* less than or equal to
2 <= 3;

//* and
2 && 3;

//* or
2 || 3;